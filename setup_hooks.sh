#!/bin/sh

# Git hooks セットアップスクリプト

echo "=========================================================="
echo "⚙️  Git pre-commit hooks のセットアップを開始します..."
echo "=========================================================="

# ターゲットディレクトリの確認
if [ ! -d ".git" ]; then
    echo "🚨 ERROR: .git ディレクトリが見つかりません。プロジェクトのルートで実行してください。"
    exit 1
fi

HOOKS_DIR=".git/hooks"
mkdir -p "$HOOKS_DIR"

# スクリプトのコピー
cp scripts/pre-commit "$HOOKS_DIR/pre-commit"

# 実行権限の付与
chmod +x "$HOOKS_DIR/pre-commit"

echo "=========================================================="
echo "✅ セットアップが完了しました。"
echo "コミット時に API キーや絶対パスの混入が自動検証されます。"
echo "=========================================================="

exit 0
